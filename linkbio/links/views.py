def _get_client_ip(request) -> str:
    forwarded_for = request.META.get("HTTP_X_FORWARDED_FOR")
    if forwarded_for:
        return forwarded_for.split(",")[0].strip()
    return request.META.get("REMOTE_ADDR", "unknown")


def _fortune_cache_key(ip_address: str, date) -> str:
    return f"lucky:fortune:{ip_address}:{date.isoformat()}"


def _share_token_cache_key(token: str) -> str:
    return f"lucky:share:token:{token}"


def _share_done_cache_key(token: str) -> str:
    return f"lucky:share:done:{token}"


def _seconds_until_midnight() -> int:
    now = timezone.localtime()
    tomorrow = (now + timedelta(days=1)).replace(hour=0, minute=0, second=0, microsecond=0)
    return max(int((tomorrow - now).total_seconds()), 0)


def fortune_status(request):
    ip_address = _get_client_ip(request)
    today = timezone.localdate()
    cache_key = _fortune_cache_key(ip_address, today)
    cached = cache.get(cache_key)
    action = request.GET.get("action")

    if action == "reset":
        cache.delete(cache_key)
        return JsonResponse(
            {
                "status": "available",
                "message": "포춘쿠키를 다시 뽑을 수 있어요!",
            }
        )

    if cached is not None:
        index = cached.get("index", 0)
        fortune = FORTUNES[index % len(FORTUNES)]
        return JsonResponse(
            {
                "status": "already_drawn",
                "index": index,
                "fortune": fortune,
                "message": "이미 오늘의 포춘쿠키를 뽑았어요. 내일 다시 와주세요!",
            }
        )

    if action == "draw":
        index = random.randrange(len(FORTUNES))
        cache.set(cache_key, {"index": index}, timeout=_seconds_until_midnight())
        return JsonResponse(
            {
                "status": "drawn",
                "index": index,
                "fortune": FORTUNES[index],
            }
        )

    return JsonResponse({"status": "available"})


def fortune_share_token(request):
    ip_address = _get_client_ip(request)
    today = timezone.localdate()
    cache_key = _fortune_cache_key(ip_address, today)
    token = secrets.token_urlsafe(16)
    cache.set(_share_token_cache_key(token), cache_key, timeout=600)
    return JsonResponse({"token": token})


def fortune_share_status(request):
    token = request.GET.get("token")
    if not token:
        return JsonResponse({"status": "missing"}, status=400)
    if cache.get(_share_done_cache_key(token)):
        cache.delete(_share_done_cache_key(token))
        return JsonResponse({"status": "shared"})
    return JsonResponse({"status": "pending"})


